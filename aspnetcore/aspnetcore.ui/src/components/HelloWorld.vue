<script>
import axios from 'axios'
export default {
  name: 'HelloWorld',
  props: {
    msg: String
  },
  data() {
    return {
      state: {
        loginData: {},
        processes: []
      }
    }
  },
  methods: {
    async loginSubmit()
    {
      const payload = this.state.loginData
      const resp = await axios.post('https://localhost:7295/api/login/login', payload)
      const data = resp.data
      if(!data.isLogin)
      {
        alert('Login failed!')
        return;
      }
      this.state.processes = data.processes;
    }
  }
}
</script>

